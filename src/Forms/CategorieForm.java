    private void AjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjouterActionPerformed
        if(cs.Create(new Categorie (codetxt.getText(),libelletxt.getText()))){
            JOptionPane.showMessageDialog(this,"Cat√©gorie est enregistr√©e");
            load();
        }
    }//GEN-LAST:event_AjouterActionPerformed

    private void SupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SupprimerActionPerformed
        // TODO add your handling code here:
        int reponse=JOptionPane.showConfirmDialog(this, "voulez-vous vraiment supprimer cette cat√©gorie");
        if(reponse==0){
            if(cs.Delete(cs.findById(id))){
                JOptionPane.showMessageDialog(this, "Cat√©gorie bien supprim√©");
                load();
            }
        }
    }//GEN-LAST:event_SupprimerActionPerformed

    private void listecategorieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listecategorieMouseClicked
        id = Integer.parseInt( model.getValueAt(listecategorie.getSelectedRow(), 0).toString());
        code = model.getValueAt(listecategorie.getSelectedRow(), 1).toString();
        libelle = model.getValueAt(listecategorie.getSelectedRow(), 2).toString();
        Load_content();
        
    }//GEN-LAST:event_listecategorieMouseClicked

    private void ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierActionPerformed
        int reponse=JOptionPane.showConfirmDialog(this, "voulez-vous vraiment modifier cette cat√©gorie");
        if(reponse==0){
            Categorie c = new Categorie(id,codetxt.getText(),libelletxt.getText());
            if(cs.Update(c)){
                JOptionPane.showMessageDialog(this, "Client bien modifier");
                load();
            }
        }
    }//GEN-LAST:event_ModifierActionPerformed


