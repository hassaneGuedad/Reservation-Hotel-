        public void load(){
            model.setRowCount(0);
            for(Categorie cat : cs.findAll()){
                model.addRow(new Object[]{
                    cat.getId_cat(),
                    cat.getCode(),
                    cat.getLibelle(),
                });
            }
        }
        
