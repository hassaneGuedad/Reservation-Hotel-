    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        // TODO add your handling code here:
        int reponse=JOptionPane.showConfirmDialog(this, "voulez-vous vraiment supprimer ce client");
        if(reponse==0){
            if(cs.Delete(cs.findById(id))){
                JOptionPane.showMessageDialog(this, "Client bien supprimé");
                Load();
            }
        }
    }//GEN-LAST:event_supprimerActionPerformed

    private void AjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjouterActionPerformed
    if(cs.Create(new Client (nomtxt.getText(),prenomtxt.getText(),teletxt.getText(),mailtxt.getText()))){
        JOptionPane.showMessageDialog(this,"Client est enregistré");
        Load();
    }
    }//GEN-LAST:event_AjouterActionPerformed

    private void ListeClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListeClientMouseClicked
    id = Integer.parseInt((String) model.getValueAt(ListeClient.getSelectedRow(), 0).toString());
    nom = model.getValueAt(ListeClient.getSelectedRow(), 1).toString();
    prenom = model.getValueAt(ListeClient.getSelectedRow(), 2).toString();
    telephone = model.getValueAt(ListeClient.getSelectedRow(), 3).toString();
    email = model.getValueAt(ListeClient.getSelectedRow(), 4).toString();
    Load_content();
    }//GEN-LAST:event_ListeClientMouseClicked

    private void ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierActionPerformed
            // TODO add your handling code here:
        int reponse=JOptionPane.showConfirmDialog(this, "voulez-vous vraiment modifier ce client");
        if(reponse==0){
            Client c = new Client(id,nomtxt.getText(),prenomtxt.getText(),teletxt.getText(),mailtxt.getText());
            if(cs.Update(c)){
                JOptionPane.showMessageDialog(this, "Client bien modifier");
                Load();
            }
        }
            
    }//GEN-LAST:event_ModifierActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        MessageFormat header = new MessageFormat("Liste des Clients :");
        MessageFormat footer = new MessageFormat("Page");

        try{
            ListeClient.print(JTable.PrintMode.FIT_WIDTH,header,footer);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "impossible d'imprimer");
 
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    


